namespace NexusPM.Domain.Enums;

/// <summary>
/// Represents the priority levels for an issue.
/// </summary>
public enum IssuePriority
{
    /// <summary>
    /// The lowest priority level.
    /// </summary>
    Lowest = 0,

    /// <summary>
    /// A low priority level.
    /// </summary>
    Low = 1,

    /// <summary>
    /// A medium priority level.
    /// </summary>
    Medium = 2,

    /// <summary>
    /// A high priority level.
    /// </summary>
    High = 3,

    /// <summary>
    /// The highest priority level.
    /// </summary>
    Highest = 4,
}
